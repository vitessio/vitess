func createCol(name string, typ int, collation string, def string, size, scale int32, notNullable bool, values string) vindexes.Column {
	var expr sqlparser.Expr
	if def != "" {
		var err error
		expr, err = sqlparser.ParseExpr(def)
		if err != nil {
			panic(fmt.Sprintf("Failed to parse %q: %v", def, err))
		}
	}
	var vals []string
	if values != "" {
		quotedVals := strings.Split(values, ",")
		vals = make([]string, 0, len(quotedVals))
		for _, v := range quotedVals {
			u := strings.TrimFunc(v, func(r rune) bool {
				return r == '\''
			})
			vals = append(vals, u)
		}
	}

	return vindexes.Column{
		Name:          sqlparser.NewIdentifierCI(name),
		Type:          query.Type(typ),
		CollationName: collation,
		Default:       expr,
		Size:          size,
		Scale:         scale,
		Nullable:      !notNullable,
		Values:        vals,
	}
}

