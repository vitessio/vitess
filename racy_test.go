// +build !appengine

// This program demonstrates a race condition.
// To observe the race with the race detector, build with -race.
package testrace

import (
	"fmt"
	"testing"
)

func TestRace(t *testing.T) {
	done := make(chan bool)
	m := make(map[string]string)
	m["name"] = "world"
	go func() {
		m["name"] = "data race"
		done <- true
	}()
	fmt.Println("Hello,", m["name"])
	<-done
}
